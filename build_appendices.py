B1_ROWS = [
    ('Авторизация в системе', '+', '+'),
    ('Просмотр списка экспериментов', '+', '+'),
    ('Создание эксперимента', '+', '+'),
    ('Загрузка изображений образцов', '+', '+'),
    ('Просмотр результатов и графиков', '+', '+'),
    ('Редактирование своего эксперимента', '+', '+'),
    ('Удаление эксперимента', '-', '+'),
    ('Управление типами плёнок', '-', '+'),
    ('Управление конфигурациями оборудования', '-', '+'),
    ('Управление ситуациями и причинами', '-', '+'),
    ('Управление пользователями', '-', '+'),
    ('Просмотр системных метрик', '-', '+'),
]

def rebuild_b1(tbl, user_rows):
    tbl_elem = tbl._tbl
    all_rows = tbl_elem.findall(qn('w:tr'))

    # 1. Обновляем tblGrid: 5 → 3 колонки
    tblGrid = tbl_elem.find(qn('w:tblGrid'))
    if tblGrid is not None:
        gridCols = tblGrid.findall(qn('w:gridCol'))
        total_w = sum(int(gc.get(qn('w:w'), '1800')) for gc in gridCols)
        for gc in gridCols[3:]:
            tblGrid.remove(gc)
        remaining = tblGrid.findall(qn('w:gridCol'))
        w_each = total_w // 3
        for gc in remaining:
            gc.set(qn('w:w'), str(w_each))

    # 2. Строка 0: меняем gridSpan второй ячейки 4 → 2
    row0 = all_rows[0]
    for tc in row0.findall(qn('w:tc')):
        gs = tc.find('.//' + qn('w:gridSpan'))
        if gs is not None and gs.get(qn('w:val')) == '4':
            gs.set(qn('w:val'), '2')

    # 3. Строка 1 (заголовки): удаляем ячейки 3 и 4, переименовываем
    row1 = all_rows[1]
    row1_cells = row1.findall(qn('w:tc'))
    for tc in row1_cells[3:]:
        row1.remove(tc)
    new_hdrs = ['Права', 'Технолог-исследователь', 'Администратор']
    for tc, hdr in zip(row1.findall(qn('w:tc')), new_hdrs):
        for t_elem in tc.iter(qn('w:t')):
            t_elem.text = hdr
            break

    # 4. Удаляем старые строки данных
    for row in all_rows[2:]:
        tbl_elem.remove(row)

    # 5. Шаблон строки данных — клон строки 1 без жирности
    template = copy.deepcopy(row1)
    for b_elem in template.findall('.//' + qn('w:b')):
        b_elem.getparent().remove(b_elem)

    # 6. Добавляем строки данных
    for func_name, tech, admin in user_rows:
        new_row = copy.deepcopy(template)
        cells = new_row.findall(qn('w:tc'))
        for tc, val in zip(cells, [func_name, tech, admin]):
            for t_elem in tc.iter(qn('w:t')):
                t_elem.text = val
                break
        tbl_elem.append(new_row)

rebuild_b1(tbl_b1, B1_ROWS)
