def set_table_rows(tbl, headers, rows_data):
    """Полностью заменяет содержимое таблицы."""
    # Удаляем все строки кроме первой (заголовочной)
    tbl_elem = tbl._tbl
    all_rows = tbl_elem.findall(qn('w:tr'))

    # Очищаем первую строку и задаём заголовки
    hdr_row = all_rows[0]
    cells = hdr_row.findall('.//' + qn('w:tc'))
    n_cols = len(headers)

    # Удаляем лишние столбцы из шапки (если в шаблоне их больше)
    for cell in cells[n_cols:]:
        cell.getparent().remove(cell)

    # Также удаляем лишние столбцы из tblGrid (если есть)
    tblGrid = tbl_elem.find(qn('w:tblGrid'))
    if tblGrid is not None:
        grid_cols = tblGrid.findall(qn('w:gridCol'))
        for gc in grid_cols[n_cols:]:
            tblGrid.remove(gc)

    # Обновлённый список ячеек шапки
    cells = hdr_row.findall('.//' + qn('w:tc'))
    for cell, hdr in zip(cells, headers):
        for t_elem in cell.iter(qn('w:t')):
            t_elem.text = hdr
            break

    # Удаляем строки 1..end
    for row in all_rows[1:]:
        tbl_elem.remove(row)

    # Добавляем новые строки
    for row_data in rows_data:
        new_row = copy.deepcopy(hdr_row)
        cells = new_row.findall('.//' + qn('w:tc'))
        for cell, val in zip(cells, row_data):
            for t_elem in cell.iter(qn('w:t')):
                t_elem.text = val
                break
        # Убираем жирность в ячейках данных
        for b_elem in new_row.findall('.//' + qn('w:b')):
            b_elem.getparent().remove(b_elem)
        tbl_elem.append(new_row)

