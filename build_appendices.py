def replace_text_in_element(elem, replacements):
    """Рекурсивно заменяет текст в w:t элементах (только внутри одного w:t)."""
    for t_elem in elem.iter(qn('w:t')):
        if t_elem.text:
            for old, new in replacements:
                if old in t_elem.text:
                    t_elem.text = t_elem.text.replace(old, new)

def replace_across_wt(body_elem, old_text, new_text):
    """
    Заменяет текст, который может быть разбит на несколько w:t внутри параграфа.
    Склеивает весь текст параграфа, заменяет, кладёт результат в первый w:t,
    остальные очищает.
    """
    for para in body_elem.iter(qn('w:p')):
        t_elems = list(para.iter(qn('w:t')))
        if not t_elems:
            continue
        texts = [t.text or '' for t in t_elems]
        full = ''.join(texts)
        if old_text not in full:
            continue
        new_full = full.replace(old_text, new_text)
        t_elems[0].text = new_full
        for t in t_elems[1:]:
            t.text = ''

